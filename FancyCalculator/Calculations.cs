using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace FancyCalculator
{
    class Calculations
    {

        public static decimal Addition(decimal input1, decimal input2)
        {
            return input1 + input2;
        }

        public static decimal Subtracation(decimal input1, decimal input2)
        {
            return input1 - input2;
        }

        public static decimal Multiplicaiton(decimal input1, decimal input2)
        {
            return input1 * input2;
        }

        public static decimal Division(decimal input1, decimal input2)
        {
            return input1 / input2;
        }
    }
}
